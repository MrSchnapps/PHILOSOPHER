char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int i2;

	i = 0;
	while (dest[i])
		i++;
	i2 = i;
	i = 0;
	while (src[i])
	{
		dest[i2] = src[i];
		i++;
		i2++;
	}
	dest[i2] = '\0';
	return (dest);
}
